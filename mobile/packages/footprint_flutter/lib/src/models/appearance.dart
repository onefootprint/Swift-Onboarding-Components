class FootprintAppearance {
  final String? fontSrc;
  final FootprintAppearanceRules? rules;
  final FootprintAppearanceVariables? variables;

  FootprintAppearance({
    this.fontSrc,
    this.rules,
    this.variables,
  });

  @internal
  FootprintAppearance.fromJson(Map<String, dynamic> json)
      : fontSrc = json['fontSrc'] as String?,
        rules = json['rules'] == null
            ? null
            : FootprintAppearanceRules.fromJson(json['rules']),
        variables = json['variables'] == null
            ? null
            : FootprintAppearanceVariables.fromJson(json['variables']);

  @internal
  Map<String, dynamic> toJson() {
    var map = {
      'fontSrc': fontSrc,
      'rules': rules?.toJson(),
      'variables': variables?.toJson(),
    };
    map.removeWhere((key, value) => value == null);
    return map;
  }
}
